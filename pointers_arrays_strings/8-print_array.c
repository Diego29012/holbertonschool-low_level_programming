/**
* print_array - prints n elements of an array of integers
* followed by a new line
* @a: array to be printed
* @n: number of elements to print
**/
void print_array(int *a, int n)
{
int d;

for (d = 0; d < d; d++)
{
	if (d == 0)
		printf("%d", a[d]);
	else
		printf(", %d", a[d]);
}
printf("\n");
}
